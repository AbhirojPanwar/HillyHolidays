    <h1>Auli</h1>
    <div class="text-mid">
      Auli is a lesser known hill station when compared to its counterparts - Shimla, Gulmarg and Manali. It gained prominence in the tourist map of India only after the formation of Uttarakhand in the year 2000. The slopes of Auli were the training ground for the Indian Paramilitary Forces and Indo-Tibetan Border Police Force. Today, Auli is known for its snow covered slopes and panoramic views of the surrounding lofty peaks of the Himalayas like Trishul, Nanda Devi, Kamet, Mana Parvat, Beethartoli, Nilkanth, Hathi Parbat, Ghori Parbat and Dunagiri.The word Auli stands for ‘Bugyal’ which means meadow in the regional language. Hindi and English are the languages spoken here. Auli, in its initial days, was a prominent trade centre. The trails of Auli were frequently traversed by the semi-nomadic Bhotiya tribes who maintained barter trading with Tibet for centuries. According to legend, the revered Guru Adi Shankaracharya visited Auli during 8th century AD. He built a math at Joshimath, which still is intact and is known by the name Sankaracharya Tapastali.
