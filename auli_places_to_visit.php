<div class="row" >
<div class="text-strong holloweffect" style="padding-left:20px!important;padding-right:20px !important">Auli Artificial Lake</div>
<div class="col-md-4 col-sm-6"><img src="auli_arti_lake.png" class="img-responsive img-thumbnail"/></div>
<div  class="col-md-8 col-sm-6"><div class="text-mid">Auli Artificial Lake is one of the world's highest man made lake. This lake was developed by the government in view of creating artificial snow on the new ski slopes at the time of low snow fall. Water from the lake is used to feed the snow guns stationed along the ski slopes. It assists in providing an apt skiing surface to help extend the ski season.</div></div>
</div>
<div class="hr-solid"></div>
<div class="row" >
<div class="text-strong holloweffect" style="padding-left:20px!important;padding-right:20px !important">Joshimath</div>
<div class="col-md-4 col-sm-6"><img src="shivpuri.png" class="img-responsive img-thumbnail"/></div>
<div  class="col-md-8 col-sm-6"><div class="text-mid">The town has a temple of Narasimha that has more prominence than its other shrines. Other shrines present in the town are dedicated to Ganesh, Surya, Gaurishankar, Hanuman and Naudevi. Sankaracharya Tapastali is another prominent attraction of this town.
The town is a hill station and gateway to the Valley of Flowers and is visited by tourists mainly during summer season between April and October</div></div>
</div>
<div class="hr-solid"></div>
<div class="row" >
<div class="text-strong holloweffect" style="padding-left:20px!important;padding-right:20px !important">Gurso Bugyal</div>
<div class="col-md-4 col-sm-6"><img src="gurso-bugyal.png" class="img-responsive img-thumbnail"/></div>
<div  class="col-md-8 col-sm-6"><div class="text-mid">Gurso Bugyal is located 3 kmaway from Auli, at an elevation of 3,056 m above sea level. During spring season, it transforms into a land carpeted by green pastures. The land is surrounded by forest of conifers and oak.By ropeway, tourists can reach Joshimath that is 1.5 km away from its last tower. As accommodation facilities are not available at the site, tourists can visit the place during the day and return back to Auli by night.</div></div>
</div>
<div class="hr-solid"></div>
<div class="row" >
<div class="text-strong holloweffect" style="padding-left:20px!important;padding-right:20px !important">Trishul Peak</div>
<div class="col-md-4 col-sm-6"><img src="trishul-peak.png" class="img-responsive img-thumbnail"/></div>
<div  class="col-md-8 col-sm-6"><div class="text-mid">Trishul and other peaks were used as the training area for the ski-equipped patrolling officers of the Indo-Tibetan Border Police Force. At the bottom of the slopes of the peak lies a lake, named the Roopkund. It is also known as the Mystery Lake because of the discovery of around 600 skeletal remains of humans and horses here. The Trishul Mountains can be viewed from Kausani and Bedini Bugyal.</div></div>
</div>
<div class="hr-solid"></div>
<div class="row" >
<div class="text-strong holloweffect" style="padding-left:20px!important;padding-right:20px !important">Chenab Lake</div>
<div class="col-md-4 col-sm-6"><img src="http://placehold.it/280x180" class="img-responsive img-thumbnail"/></div>
<div  class="col-md-8 col-sm-6"><div class="text-mid">Chenab Lake is located in the Dang Village, which is the last village of Joshimath. Travellers need to walk through an unpaved path, in order to reach this beautiful lake.</div></div>
</div>
<div class="hr-solid"></div>
<div class="row" >
<div class="text-strong holloweffect" style="padding-left:20px!important;padding-right:20px !important">Saildhar Tapovan</div>
<div class="col-md-4 col-sm-6"><img src="http://placehold.it/280x180" class="img-responsive img-thumbnail"/></div>
<div  class="col-md-8 col-sm-6"><div class="text-mid">Saildhar Tapovan, located 15 km from Auli, is a small village on the Joshimath Road. It is famous among the travellers for a temple and a natural hot water spring. Tourists can also visit a hot water fountain that is located 3 km away from Saildhar Tapovan in Saildhar. The water of this fountain is almost at the boiling point.
</div></div>
